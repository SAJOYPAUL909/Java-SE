public class Wrapper
{
    
    public static void main(String[] args) 
    {
        //autoboxing
        char c = 'a';
        Character d = c;
        System.out.println("D = "+d); 

        //unboxing 
        Integer i = 10;
        int j = i;
        System.out.println("j = "+j);
        
    }    


}
