class AssignmentOperator
{
    public static void main(String[] args) 
    {
        int a = 121;
        System.out.println("a += 2 :"+(a+=2));
        //System.out.println("a = "+a);
        System.out.println("a -= 2 :"+(a-=2));
        //System.out.println("a = "+a);
        System.out.println("a *= 2 :"+(a*=2));
        //System.out.println("a = "+a);
        System.out.println("a /= 2 :"+(a/=2));
        //System.out.println("a = "+a);
        System.out.println("a %= 2 :"+(a%=2));
        //System.out.println("a = "+a);       
    }
}